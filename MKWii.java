public class MKWii{
    private int CC;
    private String Cup;
   public MKWii(int CC, String Cup){
        this.CC = CC;
        this.Cup = Cup;
    }
    public int getCC() {
        return CC;
    }

    public String getCup() {
        return Cup;
    }

    public void setCC(int C) {
        this.CC = CC;
    }

    public void setCup(String Cup) {
        this.Cup = Cup;
    }

    
}